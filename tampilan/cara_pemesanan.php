<h4>
=> Klik pada tombol Beli pada buku yang ingin Anda pesan.<br><br />

=> Buku yang Anda pesan akan masuk kedalam <b>Keranjang Belanja</b>. Anda dapat melakukan perubahan jumlah buku yang
diinginkan dengan mengganti angka di kolom <b>Jumlah</b> dan jika anda ingin menambah buku yang akan dibeli klik tombol <b> 
   Beli Lagi</b>. Sedangkan untuk menghapus sebuah buku dari keranjang belanja, Klik <b>Tombol Hapus</b> yang berada di kolom 
   paling kanan.<br><br />

=> Jika sudah selesai, klik tombol <b>Selesai</b>, maka akan tampil <b>masukan data diri anda<b> pengunjung yang belum menjadi
   member silakan daftar member terlebih dahulu dan jika pembeli sudah menjadi member anda tinggal melakukan login<br><br />

=> Setelah data pembeli selesai diisikan, klik tombol <b>Proses</b>, maka akan tampil data pembeli beserta detail buku yang
   dipesannya.<br><br />

=> Kemudian untuk proses pembayaran dan detail pembayaran anda bisa lihat pada faktur yang akan dicetak, harga buku sudah
   termasuk <b>Ongkir</b><br><br />
   
=> Pembayaran melalui via transfer dengan Rekening Bank Mandiri, BCA<br><br />

=> Apabila telah melakukan pembayaran, silakan konfirmasi pembayaran anda melalui EMAIL yang ada di Kontak kami, maka buku/ barang akan segera kami kirimkan melalui via POS INDONESIA.